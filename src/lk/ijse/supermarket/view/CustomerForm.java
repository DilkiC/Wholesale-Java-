    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
         try {
            Customer customer=CustomerController.searchCustomer(txtCustId.getText());
            if(customer!=null){
                txtCustTitle.setText(customer.getTitle());
                txtCustName.setText(customer.getName());
                txtCustAddress.setText(customer.getAddress());
                txtCity.setText(customer.getCity());
                txtProvince.setText(customer.getProvince());
                txtPostalCode.setText(customer.getPostalCode());
                
            }else{
                JOptionPane.showMessageDialog(this, "Customer not found...");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Driver not found...");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String id=txtCustId.getText();
        String title=txtCustTitle.getText();
        String name=txtCustName.getText();
        String address=txtCustAddress.getText();
        String city=txtCity.getText();
        String province=txtProvince.getText();
        String postalCode=txtPostalCode.getText();
        
        Customer c1=new Customer(id,title,name,address,city,province,postalCode);
        
       try {
            boolean isAdded = CustomerController.addCustomer(c1);
            if (isAdded) {
                JOptionPane.showMessageDialog(this, "Added Success");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver not found");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            Customer customer=new Customer(txtCustId.getText() , txtCustTitle.getText(), txtCustName.getText(),txtCustAddress.getText(),txtCity.getText(),txtProvince.getText(),txtPostalCode.getText());
            boolean isUpdated=CustomerController.updateCustomer(customer);
            if (isUpdated) {
                JOptionPane.showMessageDialog(this, "Update Success");
            } else {
                JOptionPane.showMessageDialog(this, "Update Fail");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Driver not found...");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

